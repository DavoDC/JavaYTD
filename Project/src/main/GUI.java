    /**
     * Retrieve a component by its name
     *
     * @param nameQuery The wanted component's name
     * @return
     */
    public Component getComponentByName(String nameQuery) {

        // Return variable
        Component comp = null;

        // Get all components
        JRootPane jrp = (JRootPane) gui.getComponents()[0];
        Container cp = (Container) jrp.getContentPane();
        JPanel jp = (JPanel) cp.getComponents()[0];
        Component[] parts = jp.getComponents();

        // Iterate over all parts
        for (Component curComp : parts) {

            // When name matches, save and stop
            if (nameQuery.equalsIgnoreCase(curComp.getName())) {
                comp = curComp;
                break;
            }
        }

        // If no component could be found
        if (comp == null) {

            // Notify console only
            String msg = "'" + nameQuery + "'";
            msg += " component could not be found";
            System.out.println(msg);
        }

        // Return component
        return comp;
    }

    /**
     * Update output by appending string to output text area
     *
     * @param outputS
     * @param newLine
     */
    public void updateOutput(String outputS, boolean newLine) {

        // Get output text area
        JTextArea output = getOutputTA();

        // If new line wanted
        if (newLine) {

            // Add new line character
            outputS += "\n";
        }

        // Append line to output text area
        output.append(outputS);

        // Scroll to newest line
        output.setCaretPosition(output.getDocument().getLength());
    }

    /**
     * Get URL argument
     *
     * @return
     */
    public String getURLArg() {

        // Get raw URL string
        String rawURL = GUI.gui.getURLField().getText();

        // Holder
        String refURL;

        // If URL contains ampersand
        if (rawURL.contains("&")) {

            // Refine, notify and return
            refURL = refineURLtype(rawURL, "&");

        } else if (rawURL.contains("?list=")) {

            // Refine, notify and return
            refURL = refineURLtype(rawURL, "?list=");
        } else {

            // Otherwise, use raw URL
            refURL = rawURL;
        }
        
        // Quote URL
        refURL = Code.quoteS(refURL);

        // Return final URL
        return refURL;
    }

    /**
     * Refine URL
     *
     * @param rawURL
     * @return
     */
    private String refineURLtype(String rawURL, String splitS) {

        // Get split pattern
        String pattern = Pattern.quote(splitS);

        // Split up by pattern
        String[] URLparts = rawURL.split(pattern);

        // Extract first part
        String refinedURL = URLparts[0];

        // Notify
        outputln("\nRefined URL to: " + refinedURL);

        // Return refined URL
        return refinedURL;
    }

